const os = require('os')

// Platform
console.log(os.platform())

// Architect
console.log(os.arch())

// Info
console.log(os.cpus())

// Free Memory
console.log(os.freemem())

// All Memory
console.log(os.totalmem())

// Main Dir
console.log(os.homedir())

// How long system worked
console.log(os.uptime())

